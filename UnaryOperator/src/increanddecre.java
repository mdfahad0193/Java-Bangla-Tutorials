public class increanddecre {
    public static void main(String[] args) {
        int x=15;
        int y;

        y=x++;
        System.out.println(y);

        y=x;
        System.out.println(y);

        y=++x;
        System.out.println(y);

        y=--x;
        System.out.println(y);


        y=x--;
        System.out.println(y);

        y=x;
        System.out.println(y);

    }
}
